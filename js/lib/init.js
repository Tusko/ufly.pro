function sc(data){
    'use strict';
    if(data === 'h') {
        return $(window).height();
    } else if(data === 'w') {
        return document.body.clientWidth;
    } else {
        return $(window).scrollTop();
    }
}

jQuery(document).ready(function($){
    'use strict';
});

jQuery(window).load(function(){
    'use strict';
});

jQuery(window).on('resize', function(){
    'use strict';
});

